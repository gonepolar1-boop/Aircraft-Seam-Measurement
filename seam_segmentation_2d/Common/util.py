def ensure_dir(path):
    os.makedirs(path, exist_ok=True)
    return path


def ensure_dirs(*paths):
    for path in paths:
        os.makedirs(path, exist_ok=True)
    return paths


def ensure_parent_dir(path):
    parent_dir = os.path.dirname(path)
    if parent_dir:
        os.makedirs(parent_dir, exist_ok=True)
    return path


